#!/bin/bash

# إظهار رسالة ترحيب
echo "-----------------------------------"
echo "🛠️  نظام ترقية المستخدمين إلى أدمن"
echo "-----------------------------------"

# طلب البريد الإلكتروني من المستخدم
read -p "📧 أدخل البريد الإلكتروني للمستخدم: " target_email

# التحقق من أن الإدخال ليس فارغاً
if [ -z "$target_email" ]; then
    echo "❌ خطأ: لم تدخل بريد إلكتروني."
    exit 1
fi

echo "⏳ جاري المعالجة..."

# تشغيل كود Node.js لتحديث قاعدة البيانات
node set-admin.cjs "$target_email"

echo "-----------------------------------"
